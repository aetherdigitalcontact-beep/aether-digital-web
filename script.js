// Web Audio API used to generate premium, subtle artificial sounds
const AetherSound = (function () {
    let audioCtx = null;
    let enabled = false;

    // Unlock AudioContext on first user interaction (Browser policy)
    const init = () => {
        if (!audioCtx) {
            audioCtx = new (window.AudioContext || window.webkitAudioContext)();
            enabled = true;
        }
        if (audioCtx.state === 'suspended') {
            audioCtx.resume();
        }
    };

    const playTone = (freq, endFreq, type, duration, vol) => {
        if (!enabled || !audioCtx) return;

        const osc = audioCtx.createOscillator();
        const gainNode = audioCtx.createGain();

        osc.type = type;
        osc.frequency.setValueAtTime(freq, audioCtx.currentTime);
        if (endFreq) {
            osc.frequency.exponentialRampToValueAtTime(endFreq, audioCtx.currentTime + duration);
        }

        gainNode.gain.setValueAtTime(0, audioCtx.currentTime);
        gainNode.gain.linearRampToValueAtTime(vol, audioCtx.currentTime + 0.02);
        gainNode.gain.exponentialRampToValueAtTime(0.001, audioCtx.currentTime + duration);

        osc.connect(gainNode);
        gainNode.connect(audioCtx.destination);

        osc.start();
        osc.stop(audioCtx.currentTime + duration);
    };

    return {
        init,
        // Ultra-short, quiet high pitch tick for hover
        hover: () => playTone(2000, null, 'sine', 0.03, 0.01),
        // Clean, dry, solid click for buttons (no pitch drop = no "pew" sound)
        click: () => playTone(600, null, 'square', 0.04, 0.02),
        // Fast technical double tone for toggle
        toggle: () => {
            playTone(400, null, 'triangle', 0.05, 0.02);
            setTimeout(() => playTone(600, null, 'triangle', 0.05, 0.02), 50);
        }
    };
})();

